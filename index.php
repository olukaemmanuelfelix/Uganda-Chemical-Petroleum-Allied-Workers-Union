      <h1 class="kas">BACKGROUND</h1>
      <p class="parameter">The Uganda Chemical, Petroleum, and Allied Workers Union (UCPAWU) is a dynamic and forward-thinking trade union championing the rights and welfare of workers in Uganda’s chemical, petroleum, and allied industries. Recognized by the International Labour Organization (ILO) and affiliated with the Confederation of Free Trade Unions in Uganda (COFTU) and IndustriALL Global Union, UCPAWU is part of a powerful network representing millions of workers worldwide. Our union proudly serves employees across critical sectors, including chemical and pharmaceutical industries, petroleum and gas, manufacturing, and allied services, partnering with companies that drive Uganda’s economic growth. UCPAWU is committed to ensuring fair wages, safe workplaces, and the elimination of Gender-Based Violence (GBV) and Sexual Harassment (SH) through innovative training programs and robust case management tools. By negotiating transformative collective bargaining agreements (CBAs), we empower workers with a strong, united voice and provide employers with a stable, productive, and motivated workforce. Joining UCPAWU means becoming part of a vibrant community dedicated to fostering industrial harmony, creating opportunities for professional growth, and driving sustainable development. Together, we build stronger industries, protect workers' dignity, and promote mutual respect, ensuring success for both employees and employers. Partner with us today, and be part of the change shaping Uganda’s industrial future.</p>
